    private void handleRegistration() {
        String username = txtUsername.getText().trim();
        String password = new String(txtPassword.getPassword());
        String confirmPassword = new String(txtConfirmPassword.getPassword());

        if (username.isEmpty() || password.isEmpty() || confirmPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields.");
            return;
        }

        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match.");
            return;
        }

        if (FileStorage.credentialExists(username)) {
            JOptionPane.showMessageDialog(this, "Username already exists in offline records.");
            return;
        }

        boolean savedToDatabase = false;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/bankproject","root","");
            insert = con1.prepareStatement("INSERT INTO user (username,password) VALUES(?,?)");
            insert.setString(1, username);
            insert.setString(2, password);
            insert.executeUpdate();
            savedToDatabase = true;
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database unavailable. Credentials will be stored offline.");
        } finally {
            closeResources();
        }

        FileStorage.appendCredential(username, password);

        if (savedToDatabase) {
            JOptionPane.showMessageDialog(this, "Registration successful.");
        } else {
            JOptionPane.showMessageDialog(this, "Registration saved offline. Please try again when database is available.");
        }
        dispose();
    }

    private void closeResources() {
        try {
            if (insert != null) {
                insert.close();
            }
            if (con1 != null && !con1.isClosed()) {
                con1.close();
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

